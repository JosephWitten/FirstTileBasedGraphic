const canvas = document.getElementById("canvas")
const ctx = canvas.getContext("2d");
const numberOfSquares = 50
const widthOfSquare = canvas.width/numberOfSquares
const heightOfSquare = canvas.height/numberOfSquares
const charAmount  = 10

function between(min, max) {
    return Math.floor(Math.random() * (max-min) + min)
}

window.onload = async function () {

    let char = new Char(charAmount)
    let charPosArray = char.getCharPos()

    while(true) {
        this.draw()
        for (let i = 0; i < charPosArray.length; i++) {
            map[charPosArray[i][0]][charPosArray[i][1]] = 0
            charPosArray[i] = this.walk(charPosArray[i][0], charPosArray[i][1])
            map[charPosArray[i][0]][charPosArray[i][1]] = 3
    }

        await this.sleep(100)
        this.clear()
    
    }

}


function draw() {
    //Drawing map
    const mapHeight = 50;
    const mapWidth = 50;
    for (let i = 0; i < mapHeight; i++) {
        for (let j = 0; j < mapWidth; j++) {
            if (map[i][j] == 0) { 
                ctx.beginPath();
                ctx.fillStyle = "green"
                ctx.fillRect(widthOfSquare*j, heightOfSquare*i, widthOfSquare, heightOfSquare)
                ctx.stroke()
            }
            if (map[i][j] == 1) {
                ctx.beginPath();
                ctx.fillStyle = "blue"
                ctx.fillRect(widthOfSquare*j, heightOfSquare*i, widthOfSquare, heightOfSquare)
                ctx.stroke()
            }   
            if(map[i][j] == 3) {
                ctx.beginPath();
                ctx.fillStyle = "red"
                ctx.fillRect(widthOfSquare*j, heightOfSquare*i, widthOfSquare, heightOfSquare)
                ctx.stroke()
            }
            ctx.beginPath();
            ctx.strokeStyle = "grey"
            ctx.rect(widthOfSquare*j, heightOfSquare*i, widthOfSquare, heightOfSquare)
            ctx.stroke()
        }
    }
}

function walk(charWidth, charHeight) {
    oldCharPos = []
    oldCharPos.push(charWidth)
    oldCharPos.push(charHeight)
    randomDirection = between(0, 4)
    if (randomDirection == 0) {
        charHeight--;
    }
    if (randomDirection == 1) {
        charWidth++;
    }
    if (randomDirection == 2) {
        charHeight++;
    }
    if (randomDirection == 3) {
        charWidth--;
    }
    charPos = []
    charPos.push(charWidth)
    charPos.push(charHeight)
    if (map[charWidth][charHeight] == 1) {
        return oldCharPos
    } else {
        return charPos
    }
}
    

function clear() {
    ctx.clearRect(0,0, canvas.width, canvas.height)
}

function sleep(ms) {
    return new Promise(resolve => setTimeout(resolve, ms));
  }

