function die(i) {

    charPosArray.splice(i, 1)
}


function getFitness(i) {
        xDistance = FinishPoint[0] - deathPos[0]
        yDistance = FinishPoint[1] - deathPos[1]
        distance = (Math.sqrt(Math.pow(xDistance, 2) + Math.pow(yDistance, 2)))
        fitness.push(distance)
    
    return fitness

}


function draw() {
    //Drawing map
    const mapHeight = 50;
    const mapWidth = 50;
    map[FinishPoint[0]][FinishPoint[1]] = 2
    for (let i = 0; i < mapHeight; i++) {
        for (let j = 0; j < mapWidth; j++) {
            if (map[i][j] == 0) { 
                ctx.beginPath();
                ctx.fillStyle = "green"
                ctx.fillRect(widthOfSquare*j, heightOfSquare*i, widthOfSquare, heightOfSquare)
                ctx.stroke()
            }
            if (map[i][j] == 1) {
                ctx.beginPath();
                ctx.fillStyle = "blue"
                ctx.fillRect(widthOfSquare*j, heightOfSquare*i, widthOfSquare, heightOfSquare)
                ctx.stroke()
            }   
            if(map[i][j] == 3) {
                ctx.beginPath();
                ctx.fillStyle = "red"
                ctx.fillRect(widthOfSquare*j, heightOfSquare*i, widthOfSquare, heightOfSquare)
                ctx.stroke()
            }
            if(map[i][j] == 2) {
                ctx.beginPath();
                ctx.fillStyle = "yellow"
                ctx.fillRect(widthOfSquare*j, heightOfSquare*i, widthOfSquare, heightOfSquare)
                ctx.stroke()
            }
            ctx.beginPath();
            ctx.strokeStyle = "grey"
            ctx.rect(widthOfSquare*j, heightOfSquare*i, widthOfSquare, heightOfSquare)
            ctx.stroke()
        }
    }
}

function walk(charWidth, charHeight, i) {
    oldCharPos = []
    oldCharPos.push(charWidth)
    oldCharPos.push(charHeight)
    randomDirection = between(0, 4)
    if (randomDirection == 0) {
        charHeight--;
        charPath[i].push(0)
    }
    if (randomDirection == 1) {
        charWidth++;
        charPath[i].push(1)
    }
    if (randomDirection == 2) {
        charHeight++;
        charPath[i].push(2)
    }
    if (randomDirection == 3) {
        charWidth--;
        charPath[i].push(3)
    }
    charPos = []
    charPos.push(charWidth)
    charPos.push(charHeight)
    if (map[charWidth][charHeight] == 1) {
        // return oldCharPos
        deathPos = []
        deathPos = oldCharPos
    } else {
        return charPos
    }
}
    

function clear() {
    ctx.clearRect(0,0, canvas.width, canvas.height)
}

function sleep(ms) {
    return new Promise(resolve => setTimeout(resolve, ms));
}

