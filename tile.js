const canvas = document.getElementById("canvas")
const ctx = canvas.getContext("2d");
const numberOfSquares = 50
const widthOfSquare = canvas.width/numberOfSquares
const heightOfSquare = canvas.height/numberOfSquares

function between(min, max) {
    return Math.floor(Math.random() * (max-min) + min)
}

window.onload = async function () {

    charWidth = this.between(1, numberOfSquares -1)
    charHeight = this.between(1, numberOfSquares - 1)

    while(true) {
        this.draw()
        map[charWidth][charHeight] = 0
        charPos = this.walk(charWidth, charHeight)
        charWidth = charPos[0]
        charHeight = charPos[1]
        map[charWidth][charHeight] = 3

        await this.sleep(1000)
        this.clear()
    
    }

}


function draw() {
    //Drawing map
    const mapHeight = 50;
    const mapWidth = 50;
    for (let i = 0; i < mapHeight; i++) {
        for (let j = 0; j < mapWidth; j++) {
            if (map[i][j] == 0) { 
                ctx.beginPath();
                ctx.fillStyle = "green"
                ctx.fillRect(widthOfSquare*j, heightOfSquare*i, widthOfSquare, heightOfSquare)
                ctx.stroke()
            }
            if (map[i][j] == 1) {
                ctx.beginPath();
                ctx.fillStyle = "blue"
                ctx.fillRect(widthOfSquare*j, heightOfSquare*i, widthOfSquare, heightOfSquare)
                ctx.stroke()
            }   
            if(map[i][j] == 3) {
                ctx.beginPath();
                ctx.fillStyle = "red"
                ctx.fillRect(widthOfSquare*j, heightOfSquare*i, widthOfSquare, heightOfSquare)
                ctx.stroke()
            }
            ctx.beginPath();
            ctx.strokeStyle = "grey"
            ctx.rect(widthOfSquare*j, heightOfSquare*i, widthOfSquare, heightOfSquare)
            ctx.stroke()
        }
    }
}

function walk(charWidth, charHeight) {
    randomDirection = between(0, 4)
    if (randomDirection == 0) {
        charHeight--;
    }
    if (randomDirection == 1) {
        charWidth++;
    }
    if (randomDirection == 2) {
        charHeight++;
    }
    if (randomDirection == 3) {
        charWidth--;
    }
    charPos = []
    charPos.push(charWidth)
    charPos.push(charHeight)
    return charPos
}

function clear() {
    ctx.clearRect(0,0, canvas.width, canvas.height)
}

function sleep(ms) {
    return new Promise(resolve => setTimeout(resolve, ms));
  }

